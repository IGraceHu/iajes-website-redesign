create table if not exists public.users (
    id uuid primary key references auth.users(id) on delete cascade,
    fname text,
    lname text,
    email text,
    image_url text,
    role text not null default 'user'
);

create table if not exists public.webinars (
    id bigint generated by default as identity primary key,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    title text not null,
    webinar_url text not null,
    date date not null default current_date,
    webinar_thumbnail text,
    webinar_description text,
    speaker text not null,
    speaker_university text,
    speaker_image text,
    speaker_details text
);

alter table public.webinars add column if not exists created_at timestamptz default now();
