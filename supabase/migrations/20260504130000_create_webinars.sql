alter table public.webinars add column if not exists speaker_details text;

do $$
begin
    if exists (
        select 1
        from information_schema.columns
        where table_schema = 'public'
        and table_name = 'webinars'
        and column_name = 'primary_video'
    ) then
        execute 'update public.webinars set webinar_url = coalesce(webinar_url, primary_video) where webinar_url is null';
    end if;

    if exists (
        select 1
        from information_schema.columns
        where table_schema = 'public'
        and table_name = 'webinars'
        and column_name = 'thumbnail'
    ) then
        execute 'update public.webinars set webinar_thumbnail = coalesce(webinar_thumbnail, thumbnail) where webinar_thumbnail is null';
    end if;

    if exists (
        select 1
        from information_schema.columns
        where table_schema = 'public'
        and table_name = 'webinars'
        and column_name = 'subtitle'
    ) then
        execute 'update public.webinars set webinar_description = coalesce(webinar_description, subtitle) where webinar_description is null';
    end if;

    if exists (
        select 1
        from information_schema.columns
        where table_schema = 'public'
        and table_name = 'webinars'
        and column_name = 'date_text'
    ) then
        execute $backfill_dates$
            update public.webinars
            set date = case
                when date_text ~ '^\d{4}/\d{2}/\d{2}$' then to_date(date_text, 'YYYY/MM/DD')
                when date_text ~ '^\d{4}-\d{2}-\d{2}$' then date_text::date
                when date_text ~ '^\d{1,2}/\d{1,2}/\d{4}$' then to_date(date_text, 'MM/DD/YYYY')
                else null
            end
            where date is null
        $backfill_dates$;
    end if;

    if exists (
        select 1
        from information_schema.columns
        where table_schema = 'public'
        and table_name = 'webinars'
        and column_name = 'speakers'
    ) then
        execute $backfill_speakers$
            update public.webinars
            set
                speaker = coalesce(
                    speaker,
                    case
                        when jsonb_typeof(speakers) = 'array' then speakers->0->>'name'
                        when jsonb_typeof(speakers) = 'object' then speakers->>'name'
                        else null
                    end
                ),
                speaker_university = coalesce(
                    speaker_university,
                    case
                        when jsonb_typeof(speakers) = 'array' then coalesce(speakers->0->>'university', speakers->0->>'institution', speakers->0->>'affiliation')
                        when jsonb_typeof(speakers) = 'object' then coalesce(speakers->>'university', speakers->>'institution', speakers->>'affiliation')
                        else null
                    end
                ),
                speaker_image = coalesce(
                    speaker_image,
                    case
                        when jsonb_typeof(speakers) = 'array' then coalesce(speakers->0->>'image', speakers->0->>'image_url')
                        when jsonb_typeof(speakers) = 'object' then coalesce(speakers->>'image', speakers->>'image_url')
                        else null
                    end
                ),
                speaker_details = coalesce(
                    speaker_details,
                    case
                        when jsonb_typeof(speakers) = 'array' then coalesce(speakers->0->>'details', speakers->0->>'description', speakers->0->>'bio')
                        when jsonb_typeof(speakers) = 'object' then coalesce(speakers->>'details', speakers->>'description', speakers->>'bio')
                        else null
                    end
                )
            where speakers is not null
        $backfill_speakers$;
    end if;
end $$;

